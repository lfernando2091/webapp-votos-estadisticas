package com.saganet.politik.database.encuestas.edomex2017;

import java.io.Serializable;

import org.apache.ibatis.type.Alias;

import com.saganet.politik.database.catalogos.MunicipioEO;
import com.saganet.politik.modelos.JavaBeanT;
@Alias("EncuestaEdomex2017RegistroUsuarioEO")
public class RegistroUsuarioEO extends JavaBeanT implements Serializable {
/**
	 * 
	 */
	private static final long serialVersionUID = -7660160208645693942L;
private Integer id;
private String nombre;
private String primerApellido;
private String segundoApellido;
private String email;
private String movil;
private MunicipioEO municipio;
private String codigo;
private String clave;
@Override
public String toString() {
	return "RegistroUsuarioEO [id=" + id + ", nombre=" + nombre + ", primerApellido=" + primerApellido
			+ ", segundoApellido=" + segundoApellido + ", email=" + email + ", movil=" + movil + ", municipio="
			+ municipio + ", codigo=" + codigo + ", clave=" + clave + "]";
}
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public String getNombre() {
	return nombre;
}
public void setNombre(String nombre) {
	this.nombre = nombre.toUpperCase();
}
public String getPrimerApellido() {
	return primerApellido;
}
public void setPrimerApellido(String primerApellido) {
	this.primerApellido = primerApellido.toUpperCase();
}
public String getSegundoApellido() {
	return segundoApellido;
}
public void setSegundoApellido(String segundoApellido) {
	this.segundoApellido = segundoApellido.toUpperCase();
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getMovil() {
	return movil;
}
public void setMovil(String movil) {
	this.movil = movil;
}
public MunicipioEO getMunicipio() {
	return municipio;
}
public void setMunicipio(MunicipioEO municipio) {
	this.municipio = municipio;
}
public String getCodigo() {
	return codigo;
}
public void setCodigo(String codigo) {
	this.codigo = codigo;
}
public String getClave() {
	return clave;
}
public void setClave(String clave) {
	this.clave = clave;
}



}
