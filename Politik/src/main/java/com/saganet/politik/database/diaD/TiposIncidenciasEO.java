package com.saganet.politik.database.diaD;

import java.io.Serializable;
import org.apache.ibatis.type.Alias;
import com.saganet.politik.modelos.JavaBeanT;

@Alias("DiaDTiposIncidenciasEO")
public class TiposIncidenciasEO extends JavaBeanT implements Serializable {
	
	private static final long serialVersionUID = -6147451553202869253L;
	private Integer id;
	private String descripcion;
	
	@Override
	public String toString() {
		return "TiposIncidenciasEO [id=" + id + ", descripcion=" + descripcion + "]";
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
}
