package com.saganet.politik.dominios_dm;

public enum CatComunicacionDO {
	CORREO_ELECTRONICO(1, "Correo Electrónico"),
	WHATSAPP(2, "Whatsap"),
	FACEBOOK (3, "Facebook"),
	TWITTER(4, "Twitter"),
	MENSAJE_CELULAR(5,"Mensaje Celular"),
	NO_CONTESTA(6, "No contesta");

	private final String nombre;
	private final Integer id;
	
	private CatComunicacionDO(Integer id, String nombre) {
		this.nombre = nombre;
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public Integer getId() {
		return id;
	}
	
}
