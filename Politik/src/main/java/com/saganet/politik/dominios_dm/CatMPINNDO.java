package com.saganet.politik.dominios_dm;

public enum CatMPINNDO {
	MEJOR(1, "Mejor"),
	PEOR(2, "Peor"),
	IGUAL(3, "Igual"),
	NO_SABE(4, "No sabe"),
	NO_CONTESTA(5, "No contesta");

	private final String nombre;
	private final Integer id;
	
	private CatMPINNDO(Integer id, String nombre) {
		this.nombre = nombre;
		this.id = id;
	}

	public String getNombre() {
		return nombre;
	}

	public Integer getId() {
		return id;
	}
}
