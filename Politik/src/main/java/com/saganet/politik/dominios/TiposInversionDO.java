package com.saganet.politik.dominios;

public enum TiposInversionDO {
	FEDERAL("Federal"), 
	ESTATAL("Estatal"),
	MUNICIPAL("Municipal"),
	SOCIAL("Social"),
	PRIVADA("Privada"),
	OTRA("Otra");

	private final String nombre;

	private TiposInversionDO(String nombre) {
		this.nombre = nombre;
	}

	public String getNombre() {
		return nombre;
	}
}
