package com.saganet.politik.dominios;

public enum OperadoresDO {
	COUNT("Cuenta", "count(*)"),
	SUM("Suma", "sum(@)");
	
	private final String nombre;
	private final String codigo;
	
	private OperadoresDO(String nombre, String codigo){
		this.nombre = nombre;
		this.codigo = codigo;
	}
	
	public String getNombre() {
		return nombre;
	}

	public String getCodigo(){
		return codigo;
	}
}
