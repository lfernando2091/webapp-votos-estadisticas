package com.saganet.politik.dominios;

public enum NivelesDO {
	NACIONAL("Nacional", 1),
	ENTIDAD("Entidad", 2),
	DFEDERAL("Distrito Federal", 3),
	DLOCAL("Distrito Local", 3),
	MUNICIPIO("Municipio", 3),
	SECCION("Sección", 4),
	CASILLA("Casilla", 5),
	LOCALIDAD("Localidad", 4),
	MANZANA("Manzana", 5),
	GEOZONA("Geozona", 2),
	ZONA("Zona", 0);
	
	private final String nombre;
	private final Integer peso;
	
	private NivelesDO(String nombre, Integer peso){
		this.nombre = nombre;
		this.peso = peso;
	}
	
	public String getNombre(){
		return nombre;
	}
	
	public Integer getPeso(){
		return peso;
	}
}
 