package com.saganet.politik.utilidades;

import java.io.Serializable;
import mx.com.saganet.peon.database.dominios.TiposPoligonoDO;


public class Poligono implements Serializable {
	private static final long serialVersionUID = -4520270402081280254L;

	private TiposPoligonoDO tipo;
	private String llave;
	private String colorLinea;
	private double opacidadLinea;
	private int grosorLinea;
	private String colorRelleno;
	private double opacidadRelleno;

	public Poligono() {
		super();
		this.colorLinea = "BLACK";
		this.opacidadLinea = 1.0;
		this.grosorLinea = 1;
		this.colorRelleno = "ORANGE";
		this.opacidadRelleno = 1.0;
	}

	@Override
	public String toString() {
		return "Poligono [tipo=" + tipo + ", llave=" + llave + ", colorLinea=" + colorLinea + ", opacidadLinea="
				+ opacidadLinea + ", grosorLinea=" + grosorLinea + ", colorRelleno=" + colorRelleno
				+ ", opacidadRelleno=" + opacidadRelleno + "]";
	}

	public TiposPoligonoDO getTipo() {
		return tipo;
	}

	public void setTipo(TiposPoligonoDO tipo) {
		this.tipo = tipo;
	}

	public String getLlave() {
		return llave;
	}

	public void setLlave(String llave) {
		this.llave = llave;
	}

	public String getColorLinea() {
		return colorLinea;
	}

	public void setColorLinea(String colorLinea) {
		this.colorLinea = colorLinea;
	}

	public double getOpacidadLinea() {
		return opacidadLinea;
	}

	public void setOpacidadLinea(double opacidadLinea) {
		this.opacidadLinea = opacidadLinea;
	}

	public int getGrosorLinea() {
		return grosorLinea;
	}

	public void setGrosorLinea(int grosorLinea) {
		this.grosorLinea = grosorLinea;
	}

	public String getColorRelleno() {
		return colorRelleno;
	}

	public void setColorRelleno(String colorRelleno) {
		this.colorRelleno = colorRelleno;
	}

	public double getOpacidadRelleno() {
		return opacidadRelleno;
	}

	public void setOpacidadRelleno(double opacidadRelleno) {
		this.opacidadRelleno = opacidadRelleno;
	}

}
